package piscine

// Any проверяет, возвращает ли хотя бы один элемент среза true при применении функции f.
func Any(f func(string) bool, a []string) bool {
	// Проходим по каждому элементу среза a
	for _, v := range a {
		// Применяем функцию f к текущему элементу
		if f(v) {
			// Если хотя бы один элемент вернул true, сразу возвращаем true
			return true
		}
	}
	// Если ни один элемент не вернул true, возвращаем false
	return false
}

// IsNumeric проверяет, состоит ли строка только из цифр.
func IsNumeric(s string) bool {
	// Если строка пустая, возвращаем false
	if s == "" {
		return false
	}

	// Проходим по каждому символу строки
	for _, r := range s {
		// Проверяем, является ли символ цифрой
		if r < '0' || r > '9' {
			// Если хотя бы один символ не цифра, возвращаем false
			return false
		}
	}
	// Если все символы в строке — цифры, возвращаем true
	return true
}
