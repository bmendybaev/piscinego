package piscine

import "github.com/01-edu/z01"

func PrintNbr(n int) {
	// Обрабатываем случай, если число отрицательное
	if n < 0 {
		z01.PrintRune('-') // Печатаем знак минус
		if n == -2147483648 {
			// Специальный случай для минимального значения int
			PrintNbr(-(n / 10))
			z01.PrintRune('8')
			return
		}
		n = -n // Делаем число положительным
	}

	// Рекурсивно печатаем цифры
	if n >= 10 {
		PrintNbr(n / 10) // Рекурсивный вызов для старших цифр
	}
	z01.PrintRune(rune('0' + n%10)) // Печатаем младшую цифру
}
