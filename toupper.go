package piscine

func ToUpper(s string) string {
	// Преобразуем строку в срез байтов для модификации
	result := []byte(s)

	// Перебираем каждый символ строки
	for i := 0; i < len(result); i++ {
		// Проверяем, является ли символ строчной буквой
		if result[i] >= 'a' && result[i] <= 'z' {
			// Преобразуем букву в заглавную, вычитая 32 (по таблице ASCII)
			result[i] -= 32
		}
	}

	// Преобразуем срез байтов обратно в строку и возвращаем результат
	return string(result)
}
